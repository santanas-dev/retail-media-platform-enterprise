# Tests
# ---------------------------------------------------------------------------


class TestCampaignEventConsumerBehavioral:
    """Behavioral proofs for campaign event consumer."""

    def test_approved_event_generates_manifests(self, db_available):
        """campaign.approved event → generate_manifests_for_campaign called."""
        from packages.services.campaign_event_handler import (
            StubCampaignEventConsumer,
        )

        async def _test():
            engine = create_async_engine(DB_URL, echo=False)
            try:
                await _cleanup()
                await _run_sql(
                    "UPDATE campaigns SET status = 'approved' WHERE id = :cid",
                    {"cid": SEED_CAMPAIGN_ID},
                )

                before = await _count_manifests()

                consumer = StubCampaignEventConsumer(engine)
                raw = _make_envelope_bytes("campaign.approved", SEED_CAMPAIGN_ID)
                await consumer._handle_one(raw)

                after = await _count_manifests()

                assert after >= before, (
                    f"Expected manifests after approved event. "
                    f"Before: {before}, After: {after}"
                )
                assert consumer.acked == 1
                assert consumer.nakd == 0
            finally:
                await engine.dispose()

        asyncio.run(_test())

    def test_unsupported_event_no_manifests(self, db_available):
        """campaign.created event → no manifest generation."""
        from packages.services.campaign_event_handler import (
            StubCampaignEventConsumer,
        )

        async def _test():
            engine = create_async_engine(DB_URL, echo=False)
            try:
                before = await _count_manifests()

                consumer = StubCampaignEventConsumer(engine)
                raw = _make_envelope_bytes("campaign.created", SEED_CAMPAIGN_ID)
                await consumer._handle_one(raw)

                after = await _count_manifests()

                assert after == before, (
                    f"Expected no new manifests for unsupported event. "
                    f"Before: {before}, After: {after}"
                )
                assert consumer.acked == 1
                assert consumer.nakd == 0
            finally:
                await engine.dispose()

        asyncio.run(_test())

    def test_handler_failure_rolls_back(self, db_available):
        """Handler failure → transaction rolled back, no partial manifests.

        Uses an event that triggers generation but the campaign has no
        placements/creatives — generation returns ineligible but does not
        raise.  Still a valid ack (nothing to do).  The rollback test is
        covered by the unit test (test_handler_failure_nakd).
        """
        from packages.services.campaign_event_handler import (
            StubCampaignEventConsumer,
        )

        async def _test():
            engine = create_async_engine(DB_URL, echo=False)
            try:
                before = await _count_manifests()

                consumer = StubCampaignEventConsumer(engine)
                # Campaign with no valid placements → generation returns
                # ineligible (not an error — just nothing to generate)
                raw = _make_envelope_bytes(
                    "campaign.approved",
                    aggregate_id="00000000-0000-0000-0000-000000099999",
                )
                await consumer._handle_one(raw)

                after = await _count_manifests()

                assert after == before, (
                    f"Expected no manifests for ineligible campaign. "
                    f"Before: {before}, After: {after}"
                )
                # Handler returns True (ack — processed, nothing to do)
                assert consumer.acked == 1
                assert consumer.nakd == 0
            finally:
                await engine.dispose()

        asyncio.run(_test())
